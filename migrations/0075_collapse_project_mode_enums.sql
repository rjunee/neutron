-- 0075_collapse_project_mode_enums.sql
--
-- OSS-split Open refactor R6 (audit P2-12). Collapse the inert group-oriented
-- values out of the per-project `projects.privacy_mode` + `projects.billing_mode`
-- enums to match the single-owner canonical sets now declared in the wire/TS
-- types:
--
--   privacy_mode:  'private' | 'workspace' | 'public'  ->  'private' | 'public'
--   billing_mode:  'personal' | 'group_per_seat' | 'group_shared'  ->  'personal'
--
-- `'workspace'` (the group-visibility privacy tier) and the per-seat/group
-- billing values are removed-concept group residue: no code branches on
-- them, and single-owner Open has no workspace/group to share or bill against.
-- The privacy collapse maps `'workspace'` -> `'private'` (the conservative,
-- access-narrowing choice; nothing is made more public by the rename). Billing
-- collapses to the only surviving value, `'personal'`.
--
-- Forward-only DATA backfill, modeled on migration 0074 (the onboarding
-- phase-value backfill: UPDATE <table> SET <col>=<new> WHERE <col>=<old>). The
-- CHECK constraints on
-- `projects` (0038, rebuilt verbatim in 0053) are intentionally left as
-- supersets — they still ALLOW the old values at the SQLite layer. The TS
-- `PrivacyMode`/`BillingMode` types are now the authoritative write-side gate
-- (no producer emits the removed values), so a row can no longer acquire one.
-- Tightening the CHECK would require a STRICT-table rebuild whose tightened
-- INSERT-SELECT would reject the connect-domain test fixtures that still seed
-- `privacy_mode='workspace'` directly (those belong to the separate connect
-- vocab cleanup, audit P2-13 / R4); that is deferred. Because the schema is
-- unchanged, migrations/expected-schema.txt is NOT regenerated by this migration.
--
-- Precedent: 0025 / 0074 renamed enum-VALUE columns the same way; this file's
-- roundtrip test (tests/integration/migration-0075-collapse-project-mode-enums-
-- roundtrip.test.ts) mirrors migration-0025-phase-rename-roundtrip.test.ts.
--
-- Migration mechanics: additive on schema (no columns added/removed); only two
-- column VALUES change. The runner wraps the body in BEGIN/COMMIT for atomicity.
-- Idempotent at the runner level (the _migrations table records this version;
-- re-runs are skipped) AND in the SQL body (each UPDATE matches zero rows once
-- it has applied).

UPDATE projects SET privacy_mode = 'private' WHERE privacy_mode = 'workspace';
UPDATE projects SET billing_mode = 'personal' WHERE billing_mode IN ('group_per_seat', 'group_shared');
