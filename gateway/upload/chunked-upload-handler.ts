/**
 * @neutronai/gateway/upload — chunked resumable upload protocol.
 *
 * Three routes (all under `/api/upload/<source>/...` where `<source>`
 * is `chatgpt` or `claude`):
 *
 *   POST  /api/upload/<source>/start
 *     Mints an `upload_id`, creates a sparse temp file at
 *     `<owner_home>/imports/<upload_id>.part` sized to the full upload,
 *     persists a row in `upload_sessions`, returns
 *     `{ upload_id, chunk_size_bytes }`.
 *
 *   PATCH /api/upload/<source>/<upload_id>
 *     Accepts a chunk with `Content-Range: bytes <start>-<end>/<total>`.
 *     Writes the bytes at the requested offset (sparse-file rolls into a
 *     real allocation as chunks land). High-water-mark `bytes_received` is
 *     updated via SQL `MAX(...)` so a retried chunk doesn't regress the
 *     offset. On the final chunk the handler validates ZIP magic on the
 *     assembled file, moves it to `<owner_home>/imports/<source>.zip`,
 *     bridges into `engine.notifyImportUpload(...)` (same advance path as
 *     the legacy single-shot handler), DELETEs the session row, and
 *     returns `{ status: 'complete' }`. Partial-chunk PATCHes return
 *     `{ bytes_received }`.
 *
 *   HEAD  /api/upload/<source>/<upload_id>
 *     Returns 200 with `Upload-Offset: <bytes_received>` for any active
 *     session. 404 when the session is unknown OR expired (>24h since
 *     created_at OR status flipped to 'expired' by the sweeper).
 *
 * Borrows the SHAPE of tus.io but does NOT claim tus protocol
 * compatibility — no Tus-Resumable header, no creation extension, no
 * checksum extension. Lightweight by design.
 *
 * Why this exists: single-shot multipart POST on a 1.18 GB ChatGPT
 * export means a connection drop at 90% restarts from byte 0. Sam's
 * actual import on 2026-05-24 motivated the protocol replacement; this
 * handler is the server side. The client lives at
 * `landing/upload-client.ts` (web) and stays single-shot on the in-app
 * (RN) path for now (`app/lib/upload-client.ts` — out-of-scope per the
 * sprint brief).
 *
 * Sharing with the legacy single-shot handler:
 *   - MAX_UPLOAD_BYTES_DEFAULT (env-overridable size cap) is imported
 *     from `import-upload-handler.ts` so a single `NEUTRON_MAX_UPLOAD_BYTES`
 *     override applies to BOTH endpoints.
 *   - `engine.notifyImportUpload(...)` is called on completion with the
 *     same shape as the single-shot path so the InterviewEngine's
 *     `import_upload_pending` → `import_running` advance fires identically.
 *   - `X-Neutron-Topic-Id` resolution (per `import-upload-handler.ts`) is
 *     reused via the handler-input's `resolveTopicId` closure — production
 *     wires it to the same `extractTopicIdFromRequest` parser the legacy
 *     handler uses.
 */

